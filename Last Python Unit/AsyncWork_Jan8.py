# What are three reasons why programmers write documentation?

'1. Programmers use documentation to help readers and users in the future understand the basics of code.'
'An example is an app meant for translating text. You can use the documentation file to understand how to use it.'

'2. Programmers can use documentation to not only help readers understand, but to help their work-partners work together.'
'Such as when a future developer wants to improve on the code, they can use documentation as a reference to their past changes.'

'3. Programmers can use documentation to guide and record developement, and to help assist with installs and troubleshooting.'
'EX: in the readme.md file, you can assist an ongoing reader about your code by helping them with understanding backups in case of error,' 
'or to install and navigate the specific file in need'